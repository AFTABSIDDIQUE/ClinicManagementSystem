    public void AutoId()
    {
        try {
            Statement s = con.createStatement();
            rs = s.executeQuery("select max(psid) from prescription");
            rs.next();
            rs.getString("MAX(psid)");
            
            if (rs.getString("MAX(psid)") == null)
            {
                
                    lblprno.setText("PR001");
            }
            
            else{
                long id = Long.parseLong(rs.getString("MAX(psid)").substring(2, rs.getString("MAX(psid)").length()));
                id++;
                lblprno.setText("PR"+ String.format("%03d", id ));
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        
        
        

