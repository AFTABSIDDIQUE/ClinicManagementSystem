    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String presno = lblprno.getText();
        String channelno = txtchno.getText();
        String deasesetype = txtdeasese.getText();        
        String descptype = txtdescp.getText();
       
        
        try {
            if(!txtdeasese.getText().isEmpty() && !txtdescp.getText().isEmpty())
            {
            pst = con.prepareStatement("Insert into prescription (psid,channelid,doctorname,deasesetype,description) values(?,?,?,?,?)");
            pst.setString(1, presno);
            pst.setString(2, channelno);
            pst.setString(3, newdocname);
            pst.setString(4, deasesetype);
            pst.setString(5, descptype);
           
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Prescription Details Inserted Successfully !!!");
            
            AutoId();
            this.setVisible(false);
            }
            
            else
            {
               JOptionPane.showMessageDialog(this, "Please fill all Details ");
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

